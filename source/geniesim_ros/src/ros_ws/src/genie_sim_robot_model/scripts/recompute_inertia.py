def _find_target_files(paths: List[Path]) -> List[Path]:
    """Mirror diagnose_urdf.py's behaviour: when ``paths`` is empty,
    scan the cwd; when a path is a directory, recursively pick up
    ``.urdf`` and ``.xacro`` files; when a path is a file, use it
    directly."""
    if not paths:
        paths = [Path.cwd()]
    out: List[Path] = []
    seen = set()
    for p in paths:
        if not p.exists():
            print(f"  WARN: path {p} does not exist; skipping", file=sys.stderr)
            continue
        if p.is_file():
            if p.suffix in (".urdf", ".xacro") or p.name.endswith(".urdf.xacro"):
                rp = p.resolve()
                if rp not in seen:
                    seen.add(rp)
                    out.append(rp)
        else:
            for ext in ("*.urdf", "*.xacro", "*.urdf.xacro"):
                for c in p.rglob(ext):
                    rp = c.resolve()
                    if rp not in seen:
                        seen.add(rp)
                        out.append(rp)
    return sorted(out)


def _process_file(
    path: Path,
    *,
    mesh_root: Optional[Path],
    threshold: float,
    com_threshold_m: float,
    link_filter: Optional[str],
    dry_run: bool,
) -> Tuple[int, int, int]:
    """Process one file.  Returns ``(flagged, fixed, errors)``."""
    text = path.read_text(encoding="utf-8")
    links = _parse_links(text)
    if link_filter:
        links = [l for l in links if link_filter in l.name]
    if not links:
        return 0, 0, 0

    flagged = 0
    fixed = 0
    errors = 0
    new_text = text

    # Sort by inertia_element_start DESCENDING so we can patch in place
    # without shifting earlier offsets after each edit.
    links_sorted = sorted(links, key=lambda l: l.inertia_element_start, reverse=True)

    print(f"\n=== {path} ===")
    for link in sorted(links, key=lambda l: l.name):
        mesh_path = _resolve_mesh_path(link.mesh_filename, file_path=path, mesh_root=mesh_root)
        if mesh_path is None:
            print(f"  [{link.name:50}] SKIP — mesh {link.mesh_filename!r} not resolvable")
            errors += 1
            continue

        try:
            mesh = trimesh.load(mesh_path, force="mesh")
        except Exception as exc:  # noqa: BLE001
            print(f"  [{link.name:50}] SKIP — trimesh.load failed ({mesh_path}): {exc!r}")
            errors += 1
            continue
        # ``force="mesh"`` collapses a Scene to a single Trimesh; in
        # newer trimesh that already applies node transforms.  But
        # some DAE exporters write multi-part scenes where one
        # subgraph is rotated (axis-up convention swap, e.g.
        # Z-up vs Y-up).  When trimesh STILL returns a Scene we
        # call ``to_geometry()`` (modern; was ``dump(concatenate=True)``
        # before 4.x) which honours node transforms — naive
        # vertex-concat would silently keep parts at their pre-
        # transform coordinates and you'd see "COM 30 m off-origin"
        # garbage in the report.
        if isinstance(mesh, trimesh.Scene):
            try:
                if hasattr(mesh, "to_geometry"):
                    mesh = mesh.to_geometry()
                else:
                    mesh = mesh.dump(concatenate=True)
            except Exception as exc:  # noqa: BLE001
                print(f"  [{link.name:50}] SKIP — scene flatten failed ({mesh_path}): {exc!r}")
                errors += 1
                continue
        if not isinstance(mesh, trimesh.Trimesh) or mesh.volume is None:
            print(f"  [{link.name:50}] SKIP — could not produce a single Trimesh from {mesh_path}")
            errors += 1
            continue

        # Bounds sanity check: URDF convention is metres.  Anything
        # past ±5 m is almost certainly a millimetre-exported mesh
        # (common Blender / SolidWorks export footgun).  Warn loudly
        # — recomputed inertia from a mm-scale mesh would be 10⁹×
        # too large under uniform density.
        bmin, bmax = mesh.bounds
        max_extent = float(max(abs(bmin).max(), abs(bmax).max()))
        if max_extent > 5.0:
            print(
                f"  [{link.name:50}] SKIP — mesh bounds extend to {max_extent:.1f} m, "
                f"suggesting the .dae/.stl was exported in MILLIMETRES (URDF requires "
                f'metres).  Re-export the mesh in metres, or add `scale="0.001 0.001 0.001"` '
                f"to the URDF <mesh> element.  Skipping; inertia from a "
                f"mm-scale mesh would be ~10⁹× wrong."
            )
            errors += 1
            continue

        # Apply mesh scale (URDF scales the mesh before placing in link frame).
        if link.mesh_scale != (1.0, 1.0, 1.0):
            S = np.diag([*link.mesh_scale, 1.0])
            mesh = mesh.copy()
            mesh.apply_transform(S)

        try:
            new_inertia, mesh_com = _compute_inertia(mesh, link.mass, link.com_xyz, link.com_rpy)
        except Exception as exc:  # noqa: BLE001
            print(f"  [{link.name:50}] SKIP — inertia compute failed: {exc!r}")
            errors += 1
            continue

        # Diagnostics
        authored_off = _off_diag_ratio(link.authored_inertia)
        new_off = _off_diag_ratio(new_inertia)
        ev_authored = _principal_moments(link.authored_inertia)
        ev_new = _principal_moments(new_inertia)
        # principal-moment relative difference (frame-invariant):
        # compare the sum-of-absolute-difference of sorted eigenvalues
        # over the trace.  Catches "different physics" beyond rotation.
        principal_rel = float(np.abs(ev_authored - ev_new).sum() / max(ev_new.sum(), 1e-30))
        com_drift = float(np.linalg.norm(np.array(link.com_xyz) - np.array(mesh_com)))

        # Flag if either:
        #   * authored off-diag ratio exceeds threshold (the original tilt
        #     symptom) AND it would drop below threshold after recompute
        #   * principal moments disagree by > 10% (different physics)
        flag = (authored_off > threshold and new_off <= threshold) or principal_rel > 0.10
        com_flag = com_drift > com_threshold_m

        status = "FLAG" if flag else "ok  "
        print(f"  [{link.name:50}] {status}")
        print(
            f"    mass        = {link.mass:.6g}  (kept)\n"
            f"    com (URDF)  = ({link.com_xyz[0]:+.4f}, {link.com_xyz[1]:+.4f}, {link.com_xyz[2]:+.4f})  m\n"
            f"    com (mesh)  = ({mesh_com[0]:+.4f}, {mesh_com[1]:+.4f}, {mesh_com[2]:+.4f})  m  drift={com_drift*1000:.2f} mm"
            f"{' ⚠ exceeds --com-threshold' if com_flag else ''}"
        )
        ai = link.authored_inertia
        ni = new_inertia
        print(
            f"    URDF inertia: ixx={ai[0]:.4e} iyy={ai[1]:.4e} izz={ai[2]:.4e}\n"
            f"                  ixy={ai[3]:+.4e} ixz={ai[4]:+.4e} iyz={ai[5]:+.4e}  off/diag={authored_off:.3f}"
        )
        print(
            f"    mesh inertia: ixx={ni[0]:.4e} iyy={ni[1]:.4e} izz={ni[2]:.4e}\n"
            f"                  ixy={ni[3]:+.4e} ixz={ni[4]:+.4e} iyz={ni[5]:+.4e}  off/diag={new_off:.3f}"
        )
        print(f"    principal (sorted): URDF={ev_authored.tolist()}  mesh={ev_new.tolist()}  Δ_rel={principal_rel:.3f}")

        if flag:
            flagged += 1
            if not dry_run:
                # We patch the CURRENT new_text, which may already
                # carry earlier writes.  Because we iterate
                # ``links_sorted`` (descending offset) below in a
                # separate pass, this report-loop only counts; the
                # actual edits happen after.
                pass
    # --- Apply edits in a second pass (descending offset order) ---
    if not dry_run and flagged > 0:
        for link in links_sorted:
            mesh_path = _resolve_mesh_path(link.mesh_filename, file_path=path, mesh_root=mesh_root)
            if mesh_path is None:
                continue
            try:
                mesh = trimesh.load(mesh_path, force="mesh")
            except Exception:  # noqa: BLE001
                continue
            if isinstance(mesh, trimesh.Scene):
                try:
                    if hasattr(mesh, "to_geometry"):
                        mesh = mesh.to_geometry()
                    else:
                        mesh = mesh.dump(concatenate=True)
                except Exception:  # noqa: BLE001
                    continue
            if not isinstance(mesh, trimesh.Trimesh) or mesh.volume is None or mesh.volume <= 0:
                continue
            # Mm-scale safety net — match the report-loop check so we
            # never rewrite a tensor derived from a wrong-units mesh.
            bmin, bmax = mesh.bounds
            if float(max(abs(bmin).max(), abs(bmax).max())) > 5.0:
                continue
            if link.mesh_scale != (1.0, 1.0, 1.0):
                S = np.diag([*link.mesh_scale, 1.0])
                mesh = mesh.copy()
                mesh.apply_transform(S)
            try:
                new_inertia, _mesh_com = _compute_inertia(mesh, link.mass, link.com_xyz, link.com_rpy)
            except Exception:  # noqa: BLE001
                continue
            authored_off = _off_diag_ratio(link.authored_inertia)
            new_off = _off_diag_ratio(new_inertia)
            ev_authored = _principal_moments(link.authored_inertia)
            ev_new = _principal_moments(new_inertia)
            principal_rel = float(np.abs(ev_authored - ev_new).sum() / max(ev_new.sum(), 1e-30))
            if (authored_off > threshold and new_off <= threshold) or principal_rel > 0.10:
                new_text = _rewrite_inertia(new_text, link, new_inertia)
                fixed += 1
        if new_text != text:
            path.write_text(new_text, encoding="utf-8")
            print(f"  → wrote {fixed} fix(es) to {path}")

    return flagged, fixed, errors


def main(argv: Optional[List[str]] = None) -> int:
    parser = argparse.ArgumentParser(
        description="Recompute per-link inertia tensors from mesh geometry and "
        "(optionally) patch URDF/xacro in place.",
        epilog="See module docstring for the why, the math, and the workflow.",
    )
    parser.add_argument(
        "paths",
        nargs="*",
        type=Path,
        help="Files or directories to scan.  Default: current directory.",
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Diagnose only; do not modify any file.",
    )
    parser.add_argument(
        "--mesh-root",
        type=Path,
        default=None,
        help="Substitute ${mesh_dir} placeholders against this directory "
        "(for xacro inputs).  Also used as fallback for package:// paths.",
    )
    parser.add_argument(
        "--link",
        type=str,
        default=None,
        help="Only process links whose name contains this substring.",
    )
    parser.add_argument(
        "--threshold",
        type=float,
        default=0.15,
        help="Flag a link when authored off-diagonal magnitude exceeds "
        "this fraction of the max diagonal entry (default: 0.15).  Set "
        "to 0.0 to flag any non-zero off-diagonal.",
    )
    parser.add_argument(
        "--com-threshold",
        type=float,
        default=0.005,
        help="Report (but don't auto-fix) COM drift beyond this many "
        "metres (default: 0.005 m = 5 mm).  Operator decides whether "
        "to update the URDF <origin xyz>.",
    )
    args = parser.parse_args(argv if argv is not None else sys.argv[1:])

    files = _find_target_files(args.paths)
    if not files:
        print("No .urdf / .xacro files found.")
        return 0

    print(f"Scanned {len(files)} file(s).  threshold={args.threshold}  mesh_root={args.mesh_root}")

    total_flagged = 0
    total_fixed = 0
    total_errors = 0
    for f in files:
        fl, fx, er = _process_file(
            f,
            mesh_root=args.mesh_root,
            threshold=args.threshold,
            com_threshold_m=args.com_threshold,
            link_filter=args.link,
            dry_run=args.dry_run,
        )
        total_flagged += fl
        total_fixed += fx
        total_errors += er

    print()
    print(f"Summary: flagged={total_flagged}  fixed={total_fixed}  errors={total_errors}  " f"(dry_run={args.dry_run})")
    if total_errors > 0:
        return 2
    if args.dry_run and total_flagged > 0:
        return 1
    if not args.dry_run and total_flagged > 0 and total_fixed == 0:
        return 1
    return 0


if __name__ == "__main__":
    sys.exit(main())
