"""Recompute per-link inertia tensors from mesh geometry and patch URDF/xacro.

What this exists for
--------------------

URDF ``<inertial>`` blocks are often hand-authored or generated by CAD
exporters that picked a coordinate frame the URDF author didn't
intend.  Symptom: ``ixx/iyy/izz`` look plausible but the off-diagonal
terms (``ixy``, ``ixz``, ``iyz``) are nearly the same magnitude as the
diagonal — meaning the inertia tensor is **highly tilted** in the link
frame, and MuJoCo / PhysX will display a rotated inertia box that
doesn't match the visible geometry.

Concretely on G2's gripper fingers (swiftpicker
``gripper_*_left_inner_link``)::

    URDF:    ixx=4.31e-6  iyy=4.97e-6  izz=9.19e-6
             ixy=-4.27e-6   ← 92% of |ixy|max, near-degenerate tilt
    Computed from STL with uniform density:
             ixx=8.92e-6  iyy=4.36e-6  izz=9.18e-6
             ixy=-0.01e-6   ← negligible

The URDF was authored with the inertia values from a CAD frame whose
axes were rotated ~45° relative to the link frame.  The simulator
faithfully renders that rotation, which makes the inertia box look
misaligned with the visible mesh.  This tool re-derives the inertia
tensor from the mesh itself (assuming uniform density), expressed at
the URDF's ``<origin>`` frame, and rewrites the ``<inertia>`` element
with the corrected values.

What it does
------------

For each ``<link>`` carrying both ``<inertial>`` and a ``<visual>`` /
``<collision>`` ``<mesh filename="...">``:

1. Resolve the mesh path (handles ``package://``, absolute, relative,
   and the ``${mesh_dir}`` placeholder common in our xacros via
   ``--mesh-root``).
2. Load the mesh through ``trimesh``.
3. Read the URDF ``<inertial>``'s authored mass + origin (xyz, rpy).
   The mass is taken as-given (operator hand-tuning preserved).
4. Compute the inertia tensor from the mesh under uniform density
   ``ρ = mass / mesh.volume``, then translate it from the mesh's
   own COM to the URDF inertial origin via the parallel-axis theorem
   and rotate by the URDF's rpy.
5. Compare authored vs. computed:
     - ``Δoff_diag`` = max(|ixy|, |ixz|, |iyz|) / max(|ixx|, |iyy|,
       |izz|) — the relative weight of off-diagonal terms.  A
       well-aligned tensor has ``Δoff_diag`` ≪ 1; values near 1 mean
       the link's principal axes are rotated near 45° from the link
       frame.
     - ``Δprincipal`` = relative difference between the SORTED
       eigenvalues of the two tensors — i.e. how much the principal
       moments themselves disagree (independent of frame rotation).
6. If ``Δoff_diag`` exceeds the threshold (default 0.15), the link is
   flagged for fixing.  With ``--dry-run`` we only report; otherwise
   the ``<inertia>`` element is rewritten in place.

Mass policy
-----------

We do NOT change ``<mass>`` — operator-calibrated mass values
(measured-against-hardware) are deliberately preserved.  Only the
inertia tensor numerics are touched.

The mass is used to set the mesh density (``ρ = m / V``), which then
scales the computed inertia tensor.  If the mesh has invalid
geometry (non-watertight, negative volume) the link is skipped with
a warning.

COM policy
----------

We do NOT change the URDF ``<origin xyz=...>`` (the COM location).
URDFs typically have COM authored deliberately (often the mesh's
volumetric COM, sometimes tuned for balance).  The tool reports the
mesh's computed COM alongside the authored value; if they differ by
more than ``--com-threshold`` (default 5 mm), the link is flagged
but the COM is left alone — operator decision.

Xacro support
-------------

When ``--mesh-root`` is supplied, the tool can resolve
``${mesh_dir}/...`` placeholders without invoking the ``xacro``
expander (which would require a ROS environment).  Auto-patching
edits the xacro's ``<inertia>`` element in place via literal
attribute rewrite — works as long as the existing inertia values
are literal numerics (which they are throughout this package).

The same link name (e.g. ``gripper_${side}_left_inner_link``)
expands to multiple URDF links (``gripper_l_..._link`` and
``gripper_r_..._link``); they share the xacro source block, so a
single edit fixes both sides.  If the L/R sides ever compute
DIFFERENT inertia (e.g. mirrored mesh with different STL data),
the tool warns and skips the patch — operator decides.

Usage
-----

Dry-run on the swiftpicker xacro, resolving the ``${mesh_dir}``
placeholder against this package's mesh directory::

    recompute_inertia.py \
        ./robots/genie/g2/xacro/gripper.swiftpicker.urdf.xacro \
        --mesh-root ./robots/genie/g2/meshes \
        --dry-run

To apply (write fixes back to the xacro)::

    recompute_inertia.py ./robots/genie/g2/xacro/gripper.swiftpicker.urdf.xacro \
        --mesh-root ./robots/genie/g2/meshes

CI gate
-------

Returns non-zero exit status when:
  * any link flagged at ``--threshold`` (default 0.15) and not
    auto-fixed (i.e. ``--dry-run`` was passed)
  * any link's mesh fails to load (path missing, invalid geometry)
  * any L/R asymmetry blocks a xacro patch

Returns 0 when all flagged links were successfully rewritten.
"""

