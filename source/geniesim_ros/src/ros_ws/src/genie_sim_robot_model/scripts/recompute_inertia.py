def _format_inertia_attrs(t: Tuple[float, float, float, float, float, float]) -> str:
    """Format inertia attributes to a stable scientific notation that
    won't lose precision and stays consistent with the existing
    project style (5 significant digits in scientific form)."""
    ixx, iyy, izz, ixy, ixz, iyz = t
    return (
        f'ixx="{ixx:.6e}" '
        f'ixy="{ixy:.6e}" '
        f'ixz="{ixz:.6e}" '
        f'iyy="{iyy:.6e}" '
        f'iyz="{iyz:.6e}" '
        f'izz="{izz:.6e}"'
    )


def _rewrite_inertia(text: str, link: LinkInertial, new_t: Tuple[float, float, float, float, float, float]) -> str:
    """Replace the link's ``<inertia .../>`` element with the new
    values.  Preserves the order of attributes by re-emitting them
    in the canonical ixx/ixy/ixz/iyy/iyz/izz order (which is the
    URDF spec ordering); any author-side ordering quirks are
    normalised by this rewrite, which is intentional."""
    attrs = _format_inertia_attrs(new_t)
    replacement = f"<inertia {attrs}/>"
    return text[: link.inertia_element_start] + replacement + text[link.inertia_element_end :]


