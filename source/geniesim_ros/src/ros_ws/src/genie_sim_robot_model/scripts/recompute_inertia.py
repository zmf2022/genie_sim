def _rpy_to_matrix(rpy: Tuple[float, float, float]) -> np.ndarray:
    """ROS / URDF rpy convention: Z-Y-X intrinsic, i.e. R = Rz(y) Ry(p) Rx(r)."""
    r, p, y = rpy
    cr, sr = np.cos(r), np.sin(r)
    cp, sp = np.cos(p), np.sin(p)
    cy, sy = np.cos(y), np.sin(y)
    Rx = np.array([[1, 0, 0], [0, cr, -sr], [0, sr, cr]])
    Ry = np.array([[cp, 0, sp], [0, 1, 0], [-sp, 0, cp]])
    Rz = np.array([[cy, -sy, 0], [sy, cy, 0], [0, 0, 1]])
    return Rz @ Ry @ Rx


def _compute_inertia(
    mesh: "trimesh.Trimesh",
    mass: float,
    target_xyz: Tuple[float, float, float],
    target_rpy: Tuple[float, float, float],
) -> Tuple[Tuple[float, float, float, float, float, float], Tuple[float, float, float]]:
    """Recompute inertia at ``(target_xyz, target_rpy)`` in the link
    frame, assuming the mesh sits at the link origin (no
    ``<visual><origin>`` rotation applied).

    Returns ``(ixx, iyy, izz, ixy, ixz, iyz)`` plus the mesh's own
    COM in link frame (for COM-drift reporting).

    Inertia at the link frame origin
    --------------------------------
    ``trimesh.moment_inertia`` returns the 3×3 tensor about the
    mesh's COM, in the mesh's local axes.  We need it expressed at
    the URDF inertial origin (which is usually but not always the
    COM) and rotated by the URDF rpy.

    Steps:
      1. Set mesh density so total mass matches authored.
      2. ``I_com`` = trimesh's inertia tensor about COM.
      3. Translate to ``target_xyz`` via parallel-axis:
         ``I_target = I_com + m·((d·d)·I₃ - d⊗d)``
         where ``d = target_xyz - com``.  (Note: the parallel-axis
         shift goes from CoM to a parallel-translated point; it
         increases inertia by adding the mass × squared-offset
         contribution.  This is the standard "inertia at offset
         point" formula.)
      4. Rotate the tensor into the URDF inertial frame:
         ``I_in_target_frame = R.T · I_target · R`` where ``R``
         maps target → link frame (so ``R.T`` maps link → target).
    """
    if mesh.volume <= 0:
        raise ValueError(f"mesh has non-positive volume: {mesh.volume!r}")
    mesh.density = mass / mesh.volume
    com = np.asarray(mesh.center_mass, dtype=float)
    I_com = np.asarray(mesh.moment_inertia, dtype=float)

    d = np.asarray(target_xyz, dtype=float) - com
    I_at_target = I_com + mass * (np.dot(d, d) * np.eye(3) - np.outer(d, d))

    R = _rpy_to_matrix(target_rpy)
    I_in_target_frame = R.T @ I_at_target @ R
    return (
        (
            float(I_in_target_frame[0, 0]),
            float(I_in_target_frame[1, 1]),
            float(I_in_target_frame[2, 2]),
            float(I_in_target_frame[0, 1]),
            float(I_in_target_frame[0, 2]),
            float(I_in_target_frame[1, 2]),
        ),
        (float(com[0]), float(com[1]), float(com[2])),
    )


def _off_diag_ratio(t: Tuple[float, float, float, float, float, float]) -> float:
    """``max(|ixy|,|ixz|,|iyz|) / max(|ixx|,|iyy|,|izz|)`` — 0 means
    axis-aligned, ~1 means near-degenerate tilt.  ``inf`` if all
    diagonal entries are zero (degenerate)."""
    ixx, iyy, izz, ixy, ixz, iyz = t
    diag_max = max(abs(ixx), abs(iyy), abs(izz))
    off_max = max(abs(ixy), abs(ixz), abs(iyz))
    if diag_max <= 0.0:
        return float("inf") if off_max > 0.0 else 0.0
    return off_max / diag_max


def _principal_moments(t: Tuple[float, float, float, float, float, float]) -> np.ndarray:
    """Sorted ascending eigenvalues of the 3×3 inertia tensor."""
    ixx, iyy, izz, ixy, ixz, iyz = t
    m = np.array([[ixx, ixy, ixz], [ixy, iyy, iyz], [ixz, iyz, izz]])
    w = np.linalg.eigvalsh(m)
    return np.sort(w)


