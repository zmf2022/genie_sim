# ---------------------------------------------------------------------------
# URDF/xacro parsing
# ---------------------------------------------------------------------------

# Match a full <link>...</link> block.  We use DOTALL so nested
# whitespace / newlines don't break the match.  Non-greedy <.*?> on
# the body so consecutive sibling links don't merge.
_LINK_RE = re.compile(r"<link\b([^>]*?)>(.*?)</link>", re.DOTALL)
_LINK_NAME_RE = re.compile(r'\bname\s*=\s*"([^"]+)"')

# <inertial>...</inertial> within a link.  Self-closing isn't valid
# for <inertial> in URDF (it must contain mass + inertia at minimum),
# so we don't need to handle that form.
_INERTIAL_RE = re.compile(r"<inertial\b[^>]*>(.*?)</inertial>", re.DOTALL)

# <mass value="..."/>  — self-closing or with separator whitespace.
_MASS_RE = re.compile(r'<mass\b[^/>]*\bvalue\s*=\s*"([^"]+)"')

# <inertia ixx="..." ixy="..." ... />.  All six entries authored
# explicitly in URDF (no defaults).  Treat self-closing only — URDF
# spec for <inertia> doesn't have child elements.
_INERTIA_RE = re.compile(r"<inertia\b([^/>]*)/?>")

# <origin xyz="x y z" rpy="r p y"/>.  Either attr can be absent;
# default to zeros.
_ORIGIN_RE = re.compile(r"<origin\b([^/>]*)/?>")

# <mesh filename="..." scale="x y z"/> inside <visual> or <collision>.
# We need the visual or collision MESH (not box / cylinder / sphere)
# to compute inertia.  Geometry primitives have analytical inertia
# (cylinder for wheels, etc.) which we don't auto-compute today.
_MESH_RE = re.compile(r'<mesh\b[^/>]*\bfilename\s*=\s*"([^"]+)"')
_MESH_SCALE_RE = re.compile(r'<mesh\b[^/>]*\bscale\s*=\s*"([^"]+)"')

# <visual>...</visual> and <collision>...</collision> for mesh+origin lookup.
_VISUAL_RE = re.compile(r"<visual\b[^>]*>(.*?)</visual>", re.DOTALL)
_COLLISION_RE = re.compile(r"<collision\b[^>]*>(.*?)</collision>", re.DOTALL)


@dataclass
class LinkInertial:
    """Parsed view of one link's ``<inertial>`` + first mesh path.

    ``inertial_block_start`` / ``inertial_block_end`` are character
    offsets into the source file text — used by the rewriter to
    locate the ``<inertia .../>`` element to replace.  We keep both
    the inertial block and the inertia element offsets so the
    rewrite is surgical: only the six attribute values change, the
    surrounding XML / formatting / comments stay byte-identical.
    """

    name: str
    mass: float
    com_xyz: Tuple[float, float, float]
    com_rpy: Tuple[float, float, float]
    authored_inertia: Tuple[float, float, float, float, float, float]
    mesh_filename: str
    mesh_scale: Tuple[float, float, float]
    inertia_element_start: int  # offset of '<inertia' in file text
    inertia_element_end: int  # offset just past '/>'

    @property
    def authored_matrix(self) -> np.ndarray:
        ixx, iyy, izz, ixy, ixz, iyz = self.authored_inertia
        return np.array([[ixx, ixy, ixz], [ixy, iyy, iyz], [ixz, iyz, izz]])


def _parse_origin(attr_str: str) -> Tuple[Tuple[float, float, float], Tuple[float, float, float]]:
    xyz: Tuple[float, float, float] = (0.0, 0.0, 0.0)
    rpy: Tuple[float, float, float] = (0.0, 0.0, 0.0)
    m = re.search(r'\bxyz\s*=\s*"([^"]+)"', attr_str)
    if m:
        parts = m.group(1).split()
        xyz = (float(parts[0]), float(parts[1]), float(parts[2]))
    m = re.search(r'\brpy\s*=\s*"([^"]+)"', attr_str)
    if m:
        parts = m.group(1).split()
        rpy = (float(parts[0]), float(parts[1]), float(parts[2]))
    return xyz, rpy


def _parse_inertia_attrs(attr_str: str) -> Optional[Tuple[float, float, float, float, float, float]]:
    vals: Dict[str, float] = {}
    for k in ("ixx", "iyy", "izz", "ixy", "ixz", "iyz"):
        m = re.search(rf'\b{k}\s*=\s*"([^"]+)"', attr_str)
        if m:
            try:
                vals[k] = float(m.group(1))
            except ValueError:
                return None
    if len(vals) != 6:
        return None
    return (vals["ixx"], vals["iyy"], vals["izz"], vals["ixy"], vals["ixz"], vals["iyz"])


def _parse_links(text: str) -> List[LinkInertial]:
    """Walk the file text and produce LinkInertial records for every
    link that carries both ``<inertial>`` and a mesh.

    Skips links without ``<inertial>`` (kinematic frames), links
    with non-mesh geometry (boxes / cylinders — those have closed-
    form inertia we don't recompute), and links whose mesh path
    can't be resolved at all (logged later).
    """
    out: List[LinkInertial] = []
    for link_match in _LINK_RE.finditer(text):
        attrs = link_match.group(1)
        body = link_match.group(2)
        nm = _LINK_NAME_RE.search(attrs)
        if not nm:
            continue
        link_name = nm.group(1)

        inertial = _INERTIAL_RE.search(body)
        if not inertial:
            continue
        inertial_text = inertial.group(1)

        # Authored mass
        mass_m = _MASS_RE.search(inertial_text)
        if not mass_m:
            continue
        try:
            mass = float(mass_m.group(1))
        except ValueError:
            continue

        # Authored origin
        origin_m = _ORIGIN_RE.search(inertial_text)
        com_xyz, com_rpy = _parse_origin(origin_m.group(1) if origin_m else "")

        # Authored inertia element + its location in the FILE (for surgical rewrite).
        inertia_m = _INERTIA_RE.search(inertial_text)
        if not inertia_m:
            continue
        authored = _parse_inertia_attrs(inertia_m.group(1))
        if authored is None:
            continue
        # File-relative offsets:
        inertial_start_in_file = link_match.start() + len("<link") + link_match.group(1).__len__() + 1
        # Simpler: locate inertia in original file directly by searching from the link's start.
        link_start_offset = link_match.start()
        link_end_offset = link_match.end()
        link_slice = text[link_start_offset:link_end_offset]
        rel_m = _INERTIA_RE.search(link_slice)
        if rel_m is None:
            continue
        inertia_element_start = link_start_offset + rel_m.start()
        inertia_element_end = link_start_offset + rel_m.end()

        # First mesh from <visual>; fall back to <collision> if none.
        mesh_filename = ""
        mesh_scale: Tuple[float, float, float] = (1.0, 1.0, 1.0)
        for blk_re in (_VISUAL_RE, _COLLISION_RE):
            bm = blk_re.search(body)
            if not bm:
                continue
            mm = _MESH_RE.search(bm.group(1))
            if mm:
                mesh_filename = mm.group(1)
                sm = _MESH_SCALE_RE.search(bm.group(1))
                if sm:
                    parts = sm.group(1).split()
                    mesh_scale = (float(parts[0]), float(parts[1]), float(parts[2]))
                break
        if not mesh_filename:
            continue

        out.append(
            LinkInertial(
                name=link_name,
                mass=mass,
                com_xyz=com_xyz,
                com_rpy=com_rpy,
                authored_inertia=authored,
                mesh_filename=mesh_filename,
                mesh_scale=mesh_scale,
                inertia_element_start=inertia_element_start,
                inertia_element_end=inertia_element_end,
            )
        )
    return out


# ---------------------------------------------------------------------------
# Mesh path resolution
# ---------------------------------------------------------------------------

_PACKAGE_PREFIX = "package://"


def _resolve_mesh_path(filename: str, *, file_path: Path, mesh_root: Optional[Path]) -> Optional[Path]:
    """Resolve a URDF/xacro mesh filename to an absolute filesystem
    path.  Returns ``None`` when the file can't be located.

    Handled cases (in order):

      1. ``${mesh_dir}/...`` placeholder — substituted with
         ``mesh_root`` if provided.
      2. ``package://<pkg>/<rest>`` — strips the prefix and walks up
         from ``file_path`` looking for a directory whose suffix
         matches.  We don't shell out to ``rospack`` since this tool
         runs in a Python-only environment.
      3. Absolute path — used as-is.
      4. Relative path — resolved against ``file_path``'s parent.
    """
    raw = filename.strip()

    # 1. ${mesh_dir} substitution
    if "${mesh_dir}" in raw and mesh_root is not None:
        cand = Path(raw.replace("${mesh_dir}", str(mesh_root)))
        if cand.exists():
            return cand
        # also try absolute resolution if it's not already absolute
        if not cand.is_absolute():
            cand = (file_path.parent / cand).resolve()
            if cand.exists():
                return cand
        return None

    # 2. package:// — strip and search up
    if raw.startswith(_PACKAGE_PREFIX):
        rest = raw[len(_PACKAGE_PREFIX) :]
        slash = rest.find("/")
        if slash < 0:
            return None
        pkg = rest[:slash]
        sub = rest[slash + 1 :]
        # Walk up the directory tree from file_path looking for a dir matching pkg name.
        cur = file_path.parent
        for _ in range(8):  # bounded — don't ascend past project root
            cand_pkg = cur / pkg
            if cand_pkg.is_dir():
                cand = cand_pkg / sub
                if cand.exists():
                    return cand
            cand_share = cur / "share" / pkg
            if cand_share.is_dir():
                cand = cand_share / sub
                if cand.exists():
                    return cand
            cur = cur.parent
        # Fall back to mesh_root if provided
        if mesh_root is not None:
            cand = mesh_root / sub
            if cand.exists():
                return cand
        return None

    # 3 + 4. Absolute or relative.
    p = Path(raw)
    if p.is_absolute():
        return p if p.exists() else None
    cand = (file_path.parent / p).resolve()
    return cand if cand.exists() else None


